package MEMOWORLD;

public class Memo {
public String name;
public String write;



public Memo(String name, String write) {
	super();
	this.name = name;
	this.write = write;
}

public void setName(String name) {
	this.name = name;
}
public String getName() {
	return name;
}


public String getWrite() {
	return write;
}

public void setWrite(String write) {
	this.write = write;
}
	
}
